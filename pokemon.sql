CREATE TABLE pokemons (
  id INTEGER PRIMARY KEY,
  name VARCHAR(255) NOT NULL,
  image_url VARCHAR(255)
);

CREATE TABLE types (
  id INTEGER PRIMARY KEY,
  name VARCHAR(255) NOT NULL,
  pokemon_id INTEGER,

  FOREIGN KEY(pokemon_id) REFERENCES pokemon(id)
);

CREATE TABLE pokepics (
  id INTEGER PRIMARY KEY,
  base_64 TEXT NOT NULL,
  pokemon_id INTEGER,

  FOREIGN KEY(pokemon_id) REFERENCES pokemon(id)
);

